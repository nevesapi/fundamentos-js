// As variaveis são "caixinhas" onde você guarda valores/informações para usar depois no seu código.

// No javascript, temos 3 formas principais de declarar variaveis

// 1 - var - forma mais antiga de se declarar variaveis
// var nome = "Mago";
// var nome = "Teste";

// 2 - let
let nome = "Mago";
console.log(nome);

// redeclaração
nome = "Aragorn";
console.log(nome);

// 3 - const
const sobrenome = "Gandalf";
console.log(sobrenome);

sobrenome = "Teste";

// convenção de nome de variaveis
// camelCase - onde a primeira palavra começa com letra minúscula e as próximas com maiúscula
const macaVermelha = "Fruta";
let idadeAluno = 30;
